class IntentRecognizer:
    """售后意图识别器"""

    def __init__(self):
        self.patterns = {}
        for intent, patterns in INTENT_PATTERNS.items():
            self.patterns[intent] = [re.compile(p, re.IGNORECASE) for p in patterns]

    def recognize(self, text: str) -> dict:
        """识别售后意图"""
        results = {}
        for intent, patterns in self.patterns.items():
            score = 0
            matches = []
            for pat in patterns:
                if pat.search(text):
                    score += 1
                    matches.append(pat.pattern)
            if score > 0:
                results[intent] = {
                    "score": min(score / max(len(patterns), 1), 1.0),
                    "matches": matches
                }

        if not results:
            return {"intent": "其他咨询", "confidence": 0.0}

        best = max(results, key=lambda k: results[k]["score"])
        return {
            "intent": best,
            "confidence": results[best]["score"],
            "all_intents": results
        }


