# 关键词提取（从消息中提取技术信息）
# ============================================================
def extract_tech_info(text: str) -> dict:
    """从用户消息中提取技术信息"""
    info = {"mcu": "", "sensors": [], "modules": [], "tools": []}

    mcu_keywords = ["STM32F103C8T6", "STM32F103RCT6", "STM32", "ESP32",
                    "Arduino", "Arduino Nano", "ESP8266", "STM32F407"]
    for m in mcu_keywords:
        if m.lower() in text.lower():
            info["mcu"] = m
            break

    sensor_keywords = ["DHT11", "DS18B20", "BH1750", "BMP280", "MQ-2", "MQ-135",
                       "MAX30102", "MPU6050", "HC-SR04", "HX711", "GPS",
                       "温湿度", "温度", "湿度", "光照", "烟雾", "心率", "血氧"]
    for s in sensor_keywords:
        if s.lower() in text.lower():
            info["sensors"].append(s)

    module_keywords = ["OLED", "LCD", "TFT", "WiFi", "蓝牙", "4G", "ESP8266",
                       "APP", "继电器", "舵机", "步进电机", "语音"]
    for m in module_keywords:
        if m.lower() in text.lower():
            info["modules"].append(m)

    tool_keywords = ["Keil", "IAR", "CubeIDE", "Arduino IDE", "ST-Link",
                     "JLINK", "串口助手", "万用表", "示波器"]
    for t in tool_keywords:
        if t.lower() in text.lower():
            info["tools"].append(t)

    return info


